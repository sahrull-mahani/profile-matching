    private function _get_datatables()
    {
        $column_search = array('judul', 'deskripsi', 'users_id', 'sumber');
        $i = 0;
        foreach ($column_search as $item) { // loop column 
            if ($_GET['search']) {
                if ($i === 0) {
                    $this->groupStart();
                    $this->like($item, $_GET['search']);
                } else {
                    $this->orLike($item, $_GET['search']);
                }
                if (count($column_search) - 1 == $i)
                    $this->groupEnd();
            }
            $i++;
        }
        if (isset($_GET['order'])) {
            $this->orderBy($_GET['sort'], $_GET['order']);
        } else {
            $this->orderBy('id', 'asc');
        }

        if (!in_groups(2)) {
            $this->where('users_id', session('user_id'));
            $this->where('status', 0);
        }
        $this->select('photo.*,u.nama_user');
        $this->join('users u', 'u.id=photo.users_id');
    }
    public function get_datatables()
